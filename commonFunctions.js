/* common functions*/

function validateEmail(emailAddress) {

    var emailRegex = /[a-z0-9!#$%&'*+\/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+\/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/;
    var match = emailAddress.toLowerCase().match(emailRegex);

    return (match !== null && match.length === 1);
}